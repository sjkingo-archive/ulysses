
#ifndef _ISO646_H
#define _ISO646_H

/* Alternate spellings of boolean and bitwise operations that are not easy
 * to type on international and non-QWERTY keyboards, as per SUS:
 * http://www.opengroup.org/onlinepubs/009695399/
 */

#define and &&
#define and_eq &=
#define bitand &
#define bitor |
#define compl ~
#define not !
#define not_eq !=
#define or ||
#define or_eq |=
#define xor ^
#define xor_eq ^=

#endif

